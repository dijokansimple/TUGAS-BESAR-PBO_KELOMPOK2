    private void loadDaftarPeminjam() throws SQLException {
    BukuDAO bukuDAO = new BukuDAO();
    List<Buku> bukuList = bukuDAO.getBuku();

    // Filter buku yang statusnya DIPINJAM atau PENDING
    List<Buku> dipinjamList = new ArrayList<>();
    for (Buku b : bukuList) {
        String status = b.getStatus().toUpperCase();
        if (status.equals("DIPINJAM") || status.equals("PENDING")) {
            dipinjamList.add(b);
        }
    }

    String[][] data = new String[dipinjamList.size()][5];

    for (int i = 0; i < dipinjamList.size(); i++) {
        Buku b = dipinjamList.get(i);
        data[i][0] = String.valueOf(b.getId());
        data[i][1] = b.getNama();
        data[i][2] = b.getAuthor();
        data[i][3] = b.getTahun();
        data[i][4] = b.getStatus();
    }

    String[] columnNames = {"Nama Peminjam", "Nama Buku", "Author", "Tahun Terbit", "Status"};
    Daftar_Peminjam.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));

    // Jika mau sembunyikan kolom ID bisa seperti ini:
    Daftar_Peminjam.getColumnModel().getColumn(0).setMinWidth(0);
    Daftar_Peminjam.getColumnModel().getColumn(0).setMaxWidth(0);
    Daftar_Peminjam.getColumnModel().getColumn(0).setWidth(0);
}

    private void updateStatusBuku(int idBuku, String newStatus) {
    try {
        java.sql.Connection conn = DBConnection.getConnection();
        java.sql.PreparedStatement ps = conn.prepareStatement(
            "UPDATE buku SET status = ? WHERE id_buku = ?"
        );
        ps.setString(1, newStatus);
        ps.setInt(2, idBuku);
        
        int updated = ps.executeUpdate();
        if (updated > 0) {
            JOptionPane.showMessageDialog(this, "Status buku berhasil diperbarui ke: " + newStatus);
            loadDaftarPeminjam(); // Refresh tabel
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui status buku.");
        }

        ps.close();
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
 
           
    private void TambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahButtonActionPerformed
   String nama = Nama.getText();
   String author = Author.getText();
   String tahun = Tahun.getText();
   String jumlah = Jumlah.getText();
   String status = jComboBox1.getSelectedItem().toString(); // dari ComboBox

   // Validasi input kosong
   if (nama.isEmpty() || author.isEmpty() || tahun.isEmpty() || jumlah.isEmpty() || status.isEmpty()) {
       JOptionPane.showMessageDialog(this, "Tolong isi semua form!", "ERROR", JOptionPane.ERROR_MESSAGE);
       return;
   }

   // Validasi tahun harus angka 4 digit
   if (!tahun.matches("\\d{4}")) {
       JOptionPane.showMessageDialog(this, "Tahun harus berupa angka 4 digit!", "ERROR", JOptionPane.ERROR_MESSAGE);
       return;
   }

   // Validasi jumlah harus angka (misalnya 1, 10, 100, dll.)
   if (!jumlah.matches("\\d+")) {
       JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "ERROR", JOptionPane.ERROR_MESSAGE);
       return;
   }

   try {
       int jumlahInt = Integer.parseInt(jumlah);  // parsing jumlah ke int
       Buku buku = new Buku(0, nama, author, tahun, jumlahInt, status);
       BukuDAO bukuDAO = new BukuDAO();
       int result = bukuDAO.insertBuku(buku);

       if (result > 0) {
           JOptionPane.showMessageDialog(this, "Data buku berhasil ditambahkan!");
           clearForm(); // Bersihkan form
           loadData();  // Muat ulang data ke tabel
       } else {
           JOptionPane.showMessageDialog(this, "Gagal menambahkan data buku.");
       }
   } catch (NumberFormatException nfe) {
       JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "ERROR", JOptionPane.ERROR_MESSAGE);
   } catch (Exception e) {
       JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
   }
    }//GEN-LAST:event_TambahButtonActionPerformed

