  private void loadData() throws SQLException {
    BukuDAO bukuDAO = new BukuDAO();
    List<Buku> bukuList = bukuDAO.getBuku();
    String[][] data = new String[bukuList.size()][6];

    for (int i = 0; i < bukuList.size(); i++) {
        data[i][0] = String.valueOf(bukuList.get(i).getId());
        data[i][1] = bukuList.get(i).getNama();
        data[i][2] = bukuList.get(i).getAuthor();
        data[i][3] = bukuList.get(i).getTahun();
        data[i][4] = String.valueOf(bukuList.get(i).getJumlah());  // konversi int ke String
        data[i][5] = bukuList.get(i).getStatus();
    }

    String[] columnNames = {"ID", "Nama Buku", "Author", "Tahun Terbit", "Jumlah Buku", "Status"};
    jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));

    // Sembunyikan kolom ID agar tidak terlihat
    jTable1.getColumnModel().getColumn(0).setMinWidth(0);
    jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
    jTable1.getColumnModel().getColumn(0).setWidth(0);
}

